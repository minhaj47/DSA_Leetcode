class Solution {
public:
    vector<int> twoSum(vector<int>& numbers, int target) {
        int n = numbers.size();
        for(int i=0, j=n-1; i<j; ){
            if(numbers[i] == target - numbers[j]){
                return {i + 1, j + 1};
            } else if(numbers[i] + numbers[j] > target) j--;
            else i++;
        }
        return {};
    }
};